var colivium = [
        ['Belorusskaya', 55.778909, 37.585280, 4],
        ['Krasnye Vorota', 55.769732, 37.650253, 5],
        ['Paveletskaya', 55.731878, 37.635048, 3],
        ['Kurskaya', 55.739573, 37.665048, 2],
      ];

function setMarkers(map) {

        var image = {
          url: 'https://www.svgrepo.com/show/103810/map-pin.svg',
          // This marker is 20 pixels wide by 32 pixels high.
          size: new google.maps.Size(10, 10),
          // The origin for this image is (0, 0).
          origin: new google.maps.Point(0, 0),
          // The anchor for this image is the base of the flagpole at (0, 32).
          anchor: new google.maps.Point(0, 10)
        };
        // Shapes define the clickable region of the icon. The type defines an HTML
        // <area> element 'poly' which traces out a polygon as a series of X,Y points.
        // The final coordinate closes the poly by connecting to the first coordinate.
        var shape = {
          coords: [1, 1, 1, 20, 18, 20, 18, 1],
          type: 'poly'
        };
        for (var i = 0; i < colivium.length; i++) {
          var beach = colivium[i];
          var marker = new google.maps.Marker({
            position: {lat: beach[1], lng: beach[2]},
            map: map,
            icon: image,
            shape: shape,
            title: beach[0],
            zIndex: beach[3]
          });
        }
      }
